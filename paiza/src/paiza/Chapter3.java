package paiza;

/*
 * 変数とは、データの入れ物
 * 変数名の付け方
 * １文字目
 * ・アルファベット
 * ・アンダーバー
 *
 * ２文字目
 * ・アルファベット
 * ・アンダーバー
 * ・数字
 */

public class Chapter3 {
	public static void main(String[] args) {
		// String型を持つ変数greetingに「Hello paiza」を代入
        String greeting = "Hello paiza";

        // greetingに入っている値を表示する
		System.out.println(greeting);
		System.out.println(greeting);

		// int型を持つ変数numberに100を代入する
		int number = 100;
		System.out.println(number);
    }
}
